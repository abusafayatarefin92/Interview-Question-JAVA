package com.coderbd.ex30.datatype;

public class DefaultvalueOfDataTypes {
    //Fields Declaration
    private static boolean aBoolean;
    private static char aChar;
    private static byte aByte;
    private static short aShort;
    private static int aInt;
    private static long aLong;//0L
    private static float aFloat;//0.0f
    private static double aDouble;//0.0d

    public static void main(String[] args) {
        System.out.println("aBoolean-> " + aBoolean);
        System.out.println("aChar-> " + aChar);
        System.out.println("aByte-> " + aByte);
        System.out.println("aShort-> " + aShort);
        System.out.println("aInt-> " + aInt);
        System.out.println("aLong-> " + aLong);
        System.out.println("aFloat-> " + aFloat);
        System.out.println("aDouble-> " + aDouble);
    }

}
